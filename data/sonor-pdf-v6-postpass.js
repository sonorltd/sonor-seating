/* ============================================================================
   sonor-pdf-v6-postpass.js  ·  v1.0.0  ·  2026-05-27
   Workspace-shared (canonical at workspace root, synced to consumer apps).
   ----------------------------------------------------------------------------
   Post-pass stack — runs AFTER all pages are painted but BEFORE serialisation.
   Three features lifted from CD v5.42-v5.46 to the workspace contract per the
   v6 spec:

     • bookmarks       — Acrobat outline (sidebar nav tree)
     • statusWatermark — diagonal status stamp on every body page
     • signatureLine   — small "Generated by Sonor on …" line on every page

   The page-edge tab is painted during chrome.paintPage rather than here,
   because it needs the part colour which lives in the page spec.

   Adapters opt-in via opts.postpass: ['bookmarks', 'statusWatermark', ...].
   Defaults are theme-aware (defined in the v6 orchestrator).

   Exposes window.SonorPdfV6PostPass = { run, bookmarks, statusWatermark,
                                         signatureLine }.
   ============================================================================ */
(function () {
  'use strict';

  if (window.SonorPdfV6PostPass && window.SonorPdfV6PostPass.VERSION) return;

  const H = window.SonorPdfV6Helpers;
  const T = window.SonorPdfV6Tokens;
  const D = window.SonorPdfV6Drawing;

  /**
   * Run a chain of post-pass features.
   *
   * @param {PDFDocument} pdfDoc
   * @param {object}      ctx       paint context
   * @param {Array}       pageIndex [{ page, pageSpec }] in order
   * @param {string[]}    features  ['bookmarks', 'statusWatermark', ...]
   */
  async function run(pdfDoc, ctx, pageIndex, features) {
    const list = features || [];
    for (const feat of list) {
      const fn = ALL[feat];
      if (!fn) {
        console.warn(`[SonorPdfV6PostPass] unknown feature: ${feat}`);
        continue;
      }
      try { await fn(pdfDoc, ctx, pageIndex); }
      catch (e) { console.warn(`[SonorPdfV6PostPass] feature '${feat}' failed:`, e && e.message); }
    }
  }

  /* ---- bookmarks (Acrobat outline) ----------------------------------- */
  function bookmarks(pdfDoc, ctx, pageIndex) {
    if (!pdfDoc || typeof pdfDoc.context === 'undefined') return;
    // pdf-lib doesn't ship an outline API in 1.17.x — write a minimal outline
    // dict directly. Adobe Reader / Preview / Chrome all read this format.
    try {
      const { PDFDict, PDFName, PDFNumber, PDFArray, PDFString, PDFRef, PDFHexString } = window.PDFLib;
      const catalog = pdfDoc.catalog;
      if (!catalog) return;

      // Group pages by part (collapses sequence of pages with the same partKey)
      const groups = [];
      let cur = null;
      pageIndex.forEach((p, i) => {
        const partKey = p.pageSpec.part && p.pageSpec.part.key;
        const partTitle = p.pageSpec.part && p.pageSpec.part.title;
        if (!cur || cur.partKey !== partKey) {
          cur = { partKey, partTitle, startIndex: i, pages: [] };
          groups.push(cur);
        }
        cur.pages.push({ index: i, label: p.pageSpec.title || p.pageSpec.code || `Page ${i + 1}` });
      });

      // Build outline dict + items. Top level = parts. Children = pages.
      const outlineRef = pdfDoc.context.nextRef();
      const itemRefs = [];

      groups.forEach((g, gi) => {
        const groupRef = pdfDoc.context.nextRef();
        const childRefs = g.pages.map(p => {
          const childRef = pdfDoc.context.nextRef();
          const pageRef = pdfDoc.getPage(p.index).ref;
          const itemDict = pdfDoc.context.obj({
            Title: PDFHexString.fromText(p.label),
            Parent: groupRef,
            Dest: [pageRef, PDFName.of('Fit')],
          });
          pdfDoc.context.assign(childRef, itemDict);
          return childRef;
        });
        // wire prev/next siblings on children
        childRefs.forEach((ref, i) => {
          const dict = pdfDoc.context.lookup(ref);
          if (i > 0) dict.set(PDFName.of('Prev'), childRefs[i - 1]);
          if (i < childRefs.length - 1) dict.set(PDFName.of('Next'), childRefs[i + 1]);
        });

        const groupDict = pdfDoc.context.obj({
          Title: PDFHexString.fromText(g.partTitle || g.partKey || `Part ${gi + 1}`),
          Parent: outlineRef,
          First: childRefs[0],
          Last: childRefs[childRefs.length - 1],
          Count: -childRefs.length,
        });
        pdfDoc.context.assign(groupRef, groupDict);
        itemRefs.push(groupRef);
      });
      // wire prev/next siblings on groups
      itemRefs.forEach((ref, i) => {
        const dict = pdfDoc.context.lookup(ref);
        if (i > 0) dict.set(PDFName.of('Prev'), itemRefs[i - 1]);
        if (i < itemRefs.length - 1) dict.set(PDFName.of('Next'), itemRefs[i + 1]);
      });

      const outlineDict = pdfDoc.context.obj({
        Type: PDFName.of('Outlines'),
        First: itemRefs[0],
        Last: itemRefs[itemRefs.length - 1],
        Count: itemRefs.length,
      });
      pdfDoc.context.assign(outlineRef, outlineDict);
      catalog.set(PDFName.of('Outlines'), outlineRef);
      catalog.set(PDFName.of('PageMode'), PDFName.of('UseOutlines'));
    } catch (e) {
      // pdf-lib internals can shift between versions — bookmarks are nice to
      // have, not required. Don't break the export.
      console.warn('[SonorPdfV6PostPass] bookmarks emit failed:', e && e.message);
    }
  }

  /* ---- diagonal status watermark ------------------------------------- */
  function statusWatermark(pdfDoc, ctx, pageIndex) {
    if (!ctx.meta || !ctx.meta.status) return;
    pageIndex.forEach(p => {
      // Skip cover (page 0) — cover already shows status pill prominently.
      if (p.pageSpec.kind === 'cover') return;
      D.paintStatusWatermark(p.page, ctx);
    });
  }

  /* ---- signature line ------------------------------------------------ */
  function signatureLine(pdfDoc, ctx, pageIndex) {
    const stamp = `Generated by Sonor PDF v6.0  ·  ${new Date().toISOString().slice(0, 10)}  ·  ${ctx.app || 'app'}`;
    pageIndex.forEach(p => {
      if (p.pageSpec.kind === 'cover') return;
      const { w } = T.pageSize(ctx.format || 'A3', ctx.orientation || 'landscape');
      H.drawText(p.page, stamp, {
        x: w - 50, y: 8,
        font: ctx.fonts.reg, size: 7,
        color: T.BASE.muted, align: 'right',
      });
    });
  }

  /* ---- TOC link annotations (v1.1.0 / CD v7.1.0) --------------------- */
  /**
   * Add clickable PDF link annotations to every TOC row that has a target
   * page number. Engineers reading the 71-page deliverable can Cmd-click
   * any TOC entry to jump straight to that drawing — proper engineering-
   * doc navigation. Built as a post-pass so the schedule painter stays
   * clean; replays the schedule layout math to compute row bounds.
   *
   * Requirements: pageIndex must include the TOC page(s); each one has
   * pageSpec.kind === 'toc' and pageSpec.contents.rows with the v6.0.2
   * structure ({_group} separators + {code, page, title} data rows).
   * Data rows are clickable; group separators are not.
   */
  function tocLinks(pdfDoc, ctx, pageIndex) {
    if (!pdfDoc || !window.PDFLib) return;
    const Sch = window.SonorPdfV6Schedule;
    if (!Sch) return;
    const { PDFName, PDFArray, PDFNumber, PDFString } = window.PDFLib;

    const tocPages = pageIndex.filter(p => p.pageSpec.kind === 'toc');
    if (!tocPages.length) return;

    let linkCount = 0;
    tocPages.forEach(tocEntry => {
      const tocPage = tocEntry.page;
      const tocSpec = tocEntry.pageSpec;
      const rows = (tocSpec.contents && tocSpec.contents.rows) || [];
      if (!rows.length) return;

      // Replay schedule layout from the same constants the painter uses.
      // Body bounds match what chrome.bodyBounds returns (called by painter).
      const bodyBounds = window.SonorPdfV6Chrome.bodyBounds(ctx.format || 'A3', ctx.orientation || 'landscape');
      const x = bodyBounds.x;
      const w = bodyBounds.w;
      let y = bodyBounds.y + bodyBounds.h;        // top of table

      // Subtitle adjustment (painter does `y -= 16; ... y -= 8` if present)
      if (tocSpec.subtitle) y -= 24;
      // Header row
      y -= Sch.HEADER_H;

      // Walk rows in same order as painter
      for (let i = 0; i < rows.length; i++) {
        const row = rows[i];
        const groupLabel = row._group || (row.group === true ? row.label : null);
        if (groupLabel) {
          y -= Sch.GROUP_H;
          continue;
        }
        if (row.paragraph) {
          // Paragraph rows aren't TOC data — skip without linking
          const lines = window.SonorPdfV6Helpers.wrapText(row.paragraph, { font: ctx.fonts.reg, size: 11, maxWidth: w - 2 * Sch.PAGE_PAD || 28 });
          const needed = Math.max(Sch.PARAGRAPH_H, (lines && lines.length || 1) * 14 + 6);
          y -= needed;
          continue;
        }
        // Standard data row — emit link annotation if it has a target page
        y -= Sch.ROW_H;
        const pageNumStr = row.page;
        if (!pageNumStr) continue;
        const targetPageNum = parseInt(String(pageNumStr), 10);
        if (!isFinite(targetPageNum) || targetPageNum < 1) continue;
        let targetPage;
        try { targetPage = pdfDoc.getPage(targetPageNum - 1); } catch (_) { continue; }
        if (!targetPage) continue;

        // Annotation rect — full row width so the entire line is clickable
        const annotRef = pdfDoc.context.register(
          pdfDoc.context.obj({
            Type: PDFName.of('Annot'),
            Subtype: PDFName.of('Link'),
            Rect: [x, y, x + w, y + Sch.ROW_H],
            Border: [0, 0, 0],                              // invisible border (links stay text-coloured)
            Dest: [targetPage.ref, PDFName.of('Fit')],     // jump-to-page with auto-fit zoom
          })
        );
        // Attach annotation to the TOC page
        const existing = tocPage.node.lookup(PDFName.of('Annots'), PDFArray);
        if (existing) {
          existing.push(annotRef);
        } else {
          tocPage.node.set(PDFName.of('Annots'), pdfDoc.context.obj([annotRef]));
        }
        linkCount++;
      }
    });
    if (linkCount > 0) {
      console.log('[SonorPdfV6PostPass] tocLinks ✓ ' + linkCount + ' clickable TOC entries registered');
    }
  }

  /* ---- drawing legend key (v1.2.0 / CD v7.2.0) ----------------------- */
  /**
   * Stamp a small colour legend on every drawing-kind page so an installer
   * reading the PDF cold knows what each colour means. Right now the v7-vector
   * drawings show magenta speaker rects, hatched void walls, blue fabric
   * tracks, red dashed AFL guidelines, tan stud framing, etc. — but nothing
   * in the PDF tells the reader which is which. This legend closes that.
   *
   * Placement: tucked in the top-LEFT corner of the body area, under the
   * header band, where every CT view leaves whitespace. Small enough to not
   * cover content (180×130pt). Skipped on non-drawing pages (covers / TOC /
   * dividers / schedules don't need it).
   *
   * Vector via pdf-lib; renders crisp via v7 substrate.
   */
  function drawingLegend(pdfDoc, ctx, pageIndex) {
    const DRAWING_KINDS = ['plan', 'elevation', 'section', 'drawing', 'image'];
    const LEGEND_ENTRIES = [
      { label: 'Stud framing',        swatch: '#d4a574', kind: 'fill'  },
      { label: 'Speaker (RP22)',      swatch: '#d946ef', kind: 'rect'  },
      { label: 'Fabric gripper track', swatch: '#7dd3fc', kind: 'fill'  },
      { label: 'AFL guideline (450 / 1200)', swatch: '#ef4444', kind: 'dash'  },
      { label: 'Void / existing',     swatch: '#9ca3af', kind: 'hatch' },
      { label: 'Treatment zone',      swatch: '#fde2e4', kind: 'fill'  },
    ];

    pageIndex.forEach(p => {
      const kind = p.pageSpec.kind;
      if (!DRAWING_KINDS.includes(kind)) return;
      // Skip image pages that aren't drawings (e.g. cover hero image embeds).
      // Image kind in CD adapter IS for drawings (DR-09 coffer plan etc.) so
      // include it; keep cover excluded above.
      const page = p.page;
      const bounds = window.SonorPdfV6Chrome.bodyBounds(ctx.format || 'A3', ctx.orientation || 'landscape');
      const PAD = 8;
      const ROW_H = 14;
      const SWATCH_W = 20;
      const LEGEND_W = 150;       // v7.9.3 — was 180; tighter to corner
      const LEGEND_H = LEGEND_ENTRIES.length * ROW_H + PAD * 2 + 14;
      // Top-left of body area, below the body subtitle reservation
      const x = bounds.x + 4;
      const y = bounds.y + bounds.h - LEGEND_H - 24;

      // Panel background — slightly translucent white so it doesn't fully obscure
      // any drawing content underneath (drawings typically leave this margin empty)
      try {
        page.drawRectangle({
          x, y, width: LEGEND_W, height: LEGEND_H,
          color: H.toLibRgb('#ffffff'),
          opacity: 0.92,
          borderColor: H.toLibRgb('#cbb89a'),
          borderWidth: 0.6,
        });
      } catch (_) { return; }

      // Title
      H.drawText(page, 'KEY', {
        x: x + PAD, y: y + LEGEND_H - PAD - 9,
        font: ctx.fonts.bold, size: 9, color: T.BASE.text,
        letterSpacing: 2,
      });

      // Entries
      LEGEND_ENTRIES.forEach((entry, i) => {
        const ry = y + LEGEND_H - PAD - 24 - i * ROW_H;
        const sx = x + PAD;
        // Swatch
        try {
          if (entry.kind === 'rect') {
            page.drawRectangle({
              x: sx, y: ry, width: SWATCH_W, height: 7,
              borderColor: H.toLibRgb(entry.swatch),
              borderWidth: 1.4,
              color: undefined,
            });
          } else if (entry.kind === 'dash') {
            // Dashed line representation
            for (let dx = 0; dx < SWATCH_W; dx += 4) {
              page.drawLine({
                start: { x: sx + dx, y: ry + 3 },
                end:   { x: sx + Math.min(dx + 2, SWATCH_W), y: ry + 3 },
                color: H.toLibRgb(entry.swatch),
                thickness: 1.2,
              });
            }
          } else if (entry.kind === 'hatch') {
            // Filled box with diagonal hatch lines drawn over
            page.drawRectangle({
              x: sx, y: ry, width: SWATCH_W, height: 7,
              color: H.toLibRgb(entry.swatch),
              opacity: 0.25,
            });
            for (let dx = -7; dx < SWATCH_W; dx += 3) {
              page.drawLine({
                start: { x: sx + Math.max(0, dx),     y: ry + Math.max(0, -dx) },
                end:   { x: sx + Math.min(SWATCH_W, dx + 7), y: ry + Math.min(7, 7 - dx) },
                color: H.toLibRgb(entry.swatch),
                thickness: 0.5,
                opacity: 0.7,
              });
            }
          } else {
            // 'fill' — solid translucent
            page.drawRectangle({
              x: sx, y: ry, width: SWATCH_W, height: 7,
              color: H.toLibRgb(entry.swatch),
              opacity: 0.5,
              borderColor: H.toLibRgb(entry.swatch),
              borderWidth: 0.4,
            });
          }
        } catch (_) {}
        // Label
        H.drawText(page, entry.label, {
          x: sx + SWATCH_W + 6, y: ry + 1,
          font: ctx.fonts.reg, size: 8, color: T.BASE.text,
        });
      });
    });
  }

  /* ---- dimensions card (v1.3.0 / CD v7.3.0) -------------------------- */
  /**
   * Stamp a small "DIMENSIONS" info card top-right of every drawing page
   * so engineers can read key measurements (room W × D × H, screen W × H,
   * MLP distance) directly without flipping to a schedule. Pulls geometry
   * from `window.R.raw` (CD room state) and CT metadata where available.
   *
   * Per-view scope:
   *   - plan        → room W × D, screen wall W, MLP distance
   *   - elevation   → room W × H (or D × H), AFL guidelines (450/1200)
   *   - section/drawing/image → fallback: room W × D × H
   *
   * Placement: top-right of body area, mirroring the top-left legend KEY
   * (v7.2.0). Tight 140×95pt panel — small enough to not cover content,
   * dense enough to read at a glance.
   */
  function dimensionsCard(pdfDoc, ctx, pageIndex) {
    const DRAWING_KINDS = ['plan', 'elevation', 'section', 'drawing', 'image'];
    // v7.9.0 — Pull room geometry from ctx.meta (populated by adapter at
    // export time). Pre-v7.9.0 tried `window.R` which only worked when R
    // was on window — CD's R is a top-level `const` from build.sh's
    // concatenated bundle, NOT on window. The v6 modules run in their own
    // IIFE scope and never saw it → card silently no-op'd on every page.
    // ctx.meta is the canonical source per v7.8.0 architecture; adapter
    // sets meta.room / meta.screen / meta.mlpMm in _collectMetaSanitised.
    const meta = ctx.meta || {};
    let R = (meta.room && meta.room.w) ? meta.room : null;
    // Fallback to window.R if adapter didn't populate (older adapters)
    if (!R) {
      try {
        if (typeof window !== 'undefined' && window.R && window.R.raw) R = window.R.raw;
      } catch (_) {}
    }
    // Last resort — CT state
    let ct = null;
    try {
      if (typeof window !== 'undefined' && window.SonorCT && window.SonorCT.getState) {
        const s = window.SonorCT.getState();
        if (s && s.metadata) ct = s.metadata;
      }
    } catch (_) {}
    if (!R || !R.w || !R.d || !R.h) return;          // nothing to show

    // Pull per-view-relevant data from meta first, CT as fallback
    const screenW = (meta.screen && +meta.screen.w) || (ct && ct.screen && +ct.screen.w) || 0;
    const screenH = (meta.screen && +meta.screen.h) || (ct && ct.screen && +ct.screen.h) || 0;
    const mlpMm   = +meta.mlpMm ||
                    (ct && ct.video && +ct.video.viewingDistance) ||
                    (ct && ct.seating && +ct.seating.viewingDistance) || 0;

    pageIndex.forEach(p => {
      const kind = p.pageSpec.kind;
      if (!DRAWING_KINDS.includes(kind)) return;

      // Decide which dimensions are relevant for this page (best-effort
      // string-matching on the page title — CD adapter titles are stable).
      const title = String(p.pageSpec.title || '').toLowerCase();
      const code  = String(p.pageSpec.code  || '').toUpperCase();
      const entries = [];
      // Always-on: room overall
      entries.push({ label: 'Room W × D × H', value: R.w + ' × ' + R.d + ' × ' + R.h + ' mm' });
      // Plan view extras
      if (title.includes('plan') || /^DR-0[19]$/.test(code)) {
        if (screenW > 0) entries.push({ label: 'Screen wall W', value: screenW + ' mm' });
        if (mlpMm > 0)   entries.push({ label: 'Viewing dist (MLP)', value: mlpMm + ' mm' });
      }
      // Elevation extras
      if (title.includes('elevation') || /^DR-0[2345]$/.test(code)) {
        entries.push({ label: 'Wall height', value: R.h + ' mm' });
        if (screenW > 0 && screenH > 0 && (title.includes('screen') || code === 'DR-02')) {
          entries.push({ label: 'Screen W × H', value: screenW + ' × ' + screenH + ' mm' });
        }
        entries.push({ label: 'AFL guidelines', value: '450 · 1200 mm' });
      }
      // Coffer / ceiling pages
      if (title.includes('coffer') || title.includes('ceiling') || /^(DR-08|DR-09|DR-10)$/.test(code)) {
        if (ct && ct.coffer) {
          const drop = +(ct.coffer.drop || 0);
          if (drop > 0) entries.push({ label: 'Coffer drop', value: drop + ' mm' });
        }
      }

      if (entries.length === 0) return;

      const page = p.page;
      const bounds = window.SonorPdfV6Chrome.bodyBounds(ctx.format || 'A3', ctx.orientation || 'landscape');
      const PAD = 8;
      const ROW_H = 13;
      const CARD_W = 170;         // v7.9.3 — was 200; tighter to corner
      const CARD_H = entries.length * ROW_H + PAD * 2 + 14;
      // Top-RIGHT of body area (legend KEY sits top-left)
      const x = bounds.x + bounds.w - CARD_W - 4;
      const y = bounds.y + bounds.h - CARD_H - 24;

      try {
        page.drawRectangle({
          x, y, width: CARD_W, height: CARD_H,
          color: H.toLibRgb('#ffffff'),
          opacity: 0.92,
          borderColor: H.toLibRgb('#9bb8c4'),
          borderWidth: 0.6,
        });
      } catch (_) { return; }

      // Title
      H.drawText(page, 'DIMENSIONS', {
        x: x + PAD, y: y + CARD_H - PAD - 9,
        font: ctx.fonts.bold, size: 9, color: T.BASE.text,
        letterSpacing: 2,
      });

      // Entries
      entries.forEach((entry, i) => {
        const ry = y + CARD_H - PAD - 24 - i * ROW_H;
        H.drawText(page, entry.label, {
          x: x + PAD, y: ry + 1,
          font: ctx.fonts.reg, size: 8, color: T.BASE.muted,
        });
        H.drawText(page, entry.value, {
          x: x + CARD_W - PAD, y: ry + 1,
          font: ctx.fonts.bold, size: 8, color: T.BASE.text,
          align: 'right',
        });
      });
    });
  }

  /* ---- measureDict (Acrobat Measure/Viewport — CAD-grade measurable PDF) ----
   * DRAWING-CAD-PDF-ROADMAP P2. Attaches a rectilinear /VP + /Measure dict to
   * each to-scale drawing page so the recipient can measure real metres/m² off
   * the PDF in Acrobat. Opt-in (apps add 'measureDict' to opts.postpass) and
   * dormant otherwise — zero effect on existing exports until enabled.
   * Scoped per page: uses the exact placed-plan rect (page.__sonorDrawRegion,
   * stashed by sonor-pdf-v6-drawing.js) when present, else a content-rect
   * fallback. Only acts on pages whose pageSpec.scale parses to a 1:R ratio. */
  function measureDict(pdfDoc, ctx, pageIndex) {
    const M = (typeof window !== 'undefined') && window.SonorPdfV6Measure;
    const PDFLib = (typeof window !== 'undefined') && window.PDFLib;
    if (!M || !PDFLib) { console.warn('[SonorPdfV6PostPass] measureDict: SonorPdfV6Measure/PDFLib not loaded — skipped'); return; }
    const SKIP = { cover: 1, toc: 1, schedule: 1, index: 1, divider: 1, blank: 1 };
    let n = 0;
    pageIndex.forEach(({ page, pageSpec }) => {
      if (!pageSpec) return;
      const ratio = M.parseRatio(pageSpec.scale);
      if (ratio == null) return;                       // not a to-scale page
      if (pageSpec.kind && SKIP[pageSpec.kind]) return; // skip non-plan pages
      let bbox;
      const reg = page.__sonorDrawRegion;               // exact plan rect (preferred)
      if (reg && isFinite(reg.x0)) {
        bbox = [reg.x0, reg.y0, reg.x1, reg.y1];
      } else {                                          // content-rect fallback
        const w = page.getWidth(), h = page.getHeight();
        const m = 36, tb = 120;                         // margin + title-block strip
        bbox = [m, m + tb, w - m, h - m];
      }
      if (M.attach({ pdfDoc, page, ratio, bbox, PDFLib })) n++;
    });
    if (n) console.log('[SonorPdfV6PostPass] measureDict ✓ ' + n + ' page(s) made measurable');
  }

  const ALL = { bookmarks, statusWatermark, signatureLine, tocLinks, drawingLegend, dimensionsCard, measureDict };

  window.SonorPdfV6PostPass = {
    VERSION: '1.5.0',
    run,
    bookmarks,
    statusWatermark,
    signatureLine,
    tocLinks,
    drawingLegend,
    dimensionsCard,
    measureDict,
  };
})();
